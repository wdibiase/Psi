
function CabeceraClientes() {
    var grid = document.getElementById("gridClientes");
    grid.rows[0].cells[0].innerText = 'Cliente';
    grid.rows[0].cells[1].innerText = 'Contrato';
    grid.rows[0].cells[2].innerText = 'Calle';
    grid.rows[0].cells[3].innerText = 'Nro';
    grid.rows[0].cells[4].innerText = 'Piso';
    grid.rows[0].cells[5].innerText = 'Dpto';
    grid.rows[0].cells[6].innerText = 'Provincia';
    return false;
}
